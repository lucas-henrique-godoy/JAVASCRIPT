// Atribuição de uma função anônima a uma variável 'v' que recebe um parâmetro 'x' e retorna 'x*2'
let v = function(x) {
    return x * 2;
}

// Chamada da função 'v' passando o argumento 5 e imprimindo o resultado no console
console.log(v(5));  // Saída esperada: 10

